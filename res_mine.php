<h2>Mining Pools</h2>
              <p>
            Peercoin is a Proof-Of-Work/Proof of Stake hybrid coin that is compatible with the same ASIC hardware used on  the bitcoin network. Initially the ratio of newly produced coins favoured mined coins over minted coins.  However over time as more coin are generated, the ratio of newly produced coins shifts to favour ones produced via Proof-Of-Stake minting. With this, the increasing popularity of Peercoin, increased minting rate, and the decreasing return on mining, competition for blocks becomes fiercer.
            Even with powerful hardware it is difficult to mine alone.	Mining pools combine the power of all participants to find block, and share the reward based on shares. Read the instructions of each pool carefully as connection protocols and software
            use can change between pools.
          </p>

